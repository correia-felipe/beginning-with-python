nota1 = 90
nota2 = 2

print(f'As notas têm o valor de {nota1} e {nota2}')
print(f'A soma resulta em {nota1 + nota2}')
print(f'A subtracao resulta em {nota1 - nota2}')
print(f'A multi resulta em {nota1 * nota2}')
print(f'A divisao resulta em {nota1 / nota2}')
print(f'O resto é de {nota1 % nota2}')

